/*
5)  Escrever um programa que incremente uma variável com valor mínimo 0 e valor 
máximo 100 e as mostre no vídeo.
*/
#include <stdio.h>
#include <stdlib.h>
int main(void){
    // variáveis
    int i;
    // saida 
    for(i=0;i<100;i++){
        printf("%i, ",i);
    }
    printf("%i.",i);
    return 0;
}
